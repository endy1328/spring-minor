CREATE TABLE TB_ORDER (
    ORDER_ID VARCHAR(20) PRIMARY KEY,
    CUSTOMER_NAME VARCHAR(100) NOT NULL,
    ORDER_STATUS VARCHAR(20) NOT NULL,
    DELIVERY_STATUS VARCHAR(20) NOT NULL,
    ORDER_AMOUNT NUMBER(12, 0) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UPDATED_AT TIMESTAMP
);

CREATE TABLE TB_ORDER_ITEM (
    ORDER_ITEM_ID NUMBER(12, 0) PRIMARY KEY,
    ORDER_ID VARCHAR(20) NOT NULL,
    PRODUCT_NAME VARCHAR(100) NOT NULL,
    PRODUCT_PRICE NUMBER(12, 0) NOT NULL,
    QUANTITY NUMBER(5, 0) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

SELECT
    O.ORDER_ID,
    O.CUSTOMER_NAME,
    O.ORDER_STATUS,
    O.DELIVERY_STATUS,
    I.PRODUCT_NAME,
    I.QUANTITY
FROM TB_ORDER O
JOIN TB_ORDER_ITEM I ON O.ORDER_ID = I.ORDER_ID
WHERE O.ORDER_STATUS = '결제완료';
